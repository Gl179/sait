<?php

// Задание имени в переменной $first_name
$first_name = "Иван";

// Задание фамилии в переменной $last_name
$last_name = "Иванов";

// Создание полного имени
$full_name = $first_name . " " . $last_name;

// Вывод полного имени
echo "Полное имя: " . $full_name . "\n";

// Вывод длины строки с полным именем
echo "Длина строки: " . strlen($full_name) . "\n";
